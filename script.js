// Function to generate four random colors
function generateRandomColors() {
  const shuffledColors = colorPool.sort(() => 0.5 - Math.random());
  return shuffledColors.slice(0, 4);
}

// Function to update the UI with new colors
function updateColors() {
  const tiles = document.querySelectorAll('.color-tile');
  const randomColors = generateRandomColors();

  tiles.forEach((tile, index) => {
    const colorDisplay = tile.querySelector('.color-display');
    const colorName = tile.querySelector('.color-name');
    
    const { hex, name } = randomColors[index];
    colorDisplay.style.backgroundColor = hex;
    colorName.textContent = name;
  });
}

// Add event listener to button
document.getElementById('generate-colors').addEventListener('click', updateColors);

// Initialize with random colors
updateColors();
